// Java Comparison Operators

package javaOperators_02;

public class ComparisonOperators {

    public static void main(String[] args) {

// ==	Equal to	x == y	
// !=	Not equal	x != y
// >	Greater than	x > y	
// <	Less than	x < y	
// >=	Greater than or equal to x >= y	
// <=	Less than or equal to	x <= y	


int x =12;
int y =10;

// ==    Equal to    x == y
System.out.println(x==y);

// !=	Not equal	x != y
System.out.println(x!=y);

// >    Greater than    x > y
System.out.println(x>y);

// <    Less than    x < y
System.out.println(x<y);

// >=    Greater than or equal to    x >= y
System.out.println(x>=y);

// <=    Less than or equal to    x <= y
System.out.println(x<=y);


    }

}
    
    

